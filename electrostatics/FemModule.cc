void FemModule::
_getE()
{
  info() << "Postprocessing E";

      ENUMERATE_ (Cell, icell, allCells()) {
        Cell cell = *icell;
        Real2 DX = _computeDxDyOfRealTRIA3(cell);
        m_E[cell].x =  -DX.x  ;
        m_E[cell].y =  -DX.y  ;
        m_E[cell].z =  0.0   ;
      }

      m_E.synchronize();
}

